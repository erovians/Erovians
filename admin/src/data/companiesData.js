// Helper functions
export const getIndustries = () => {
  const industries = new Set();
  companiesData.forEach(company => {
    company.products.forEach(product => {
      industries.add(product.category);
    });
  });
  return ['All', ...Array.from(industries)];
};

export const getTotalProducts = () => {
  return companiesData.reduce((total, company) => total + company.products.length, 0);
};

export const getAverageRating = () => {
  const total = companiesData.reduce((sum, company) => sum + company.rating, 0);
  return (total / companiesData.length).toFixed(1);
};

export const getStatusCounts = () => {
  const counts = { Active: 0, Pending: 0, Blocked: 0 };
  companiesData.forEach(company => {
    counts[company.status]++;
  });
  return counts;
};